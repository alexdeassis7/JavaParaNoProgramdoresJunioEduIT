package clase3dowhile;

public class Clase3DoWhile {

    public static void main(String[] args) {
      int contador = 0;  
        System.out.println("Inicio del DO While");
        do{
            System.out.println("instruccion 1");
            System.out.println("instruccion 2");
            System.out.println("instruccion 3");
            System.out.println("instruccion n");
            contador ++ ;
        }while( contador < 3 );
        System.out.println("FIN del DO While");
    }
    
}
