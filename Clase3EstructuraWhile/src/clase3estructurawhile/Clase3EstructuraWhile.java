package clase3estructurawhile;

public class Clase3EstructuraWhile {

    public static void main(String[] args) {
        int contador = 0;
        System.out.println("Inicio del while ");
        while (contador < 1000) {
            //cuerpo del while 
            System.out.println("sentencia 1");
            System.out.println("sentencia 2");
            System.out.println("sentencia 3");
            contador++;
        }

        System.out.println("Fin del while");

    }

}
